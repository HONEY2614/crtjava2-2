import java.util.*;

class Fact

{
    
public static void main(String[] args)
    
{
       
 Scanner s=new Scanner(System.in); 

 int n,r=1;
 
 System.out.println("Enter n Value");

 n=s.nextInt();
	    
while (n>=1)
	   
{
	        
 r=r*n;
	         
n=n-1;
	    
 }
	     
System.out.println(r);
      
    
}

}



	   
	   
		
